def is_sector(code):
    '''
    Returns True if this is a NAICS sector, False otherwise
    '''
    if len(code) == 2 and code != '10':
        return True
    elif len(code) == 5 and '-' in code:
        return True
    return False


def is_supersector(code):
    '''
    Returns True if this is a NAICS supersector, False otherwise
    '''
    return len(code) == 4 and code[0:2] == '10'


def is_public_administration(code):
    if code in ('1028', '92'):
        return True
    return False


def get_parent_code(code):
    '''
    Return the parent code of the passed code.  For the root ('10'), returns
    None
    '''
    if is_supersector(code) or is_sector(code):
        return '10'
    elif code == '10':
        return None
    elif len(code) == 3 and code[0:2] in ('31', '32', '33', ):
        return '31-33'
    elif len(code) == 3 and code[0:2] in ('44', '45', ):
        return '44-45'
    elif len(code) == 3 and code[0:2] in ('48', '49', ):
        return '48-49'
    else:
        return code[0:len(code)-1]
